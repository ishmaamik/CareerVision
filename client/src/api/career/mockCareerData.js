// Export individual career by ID for easy access
export const getCareerById = (id) => {
  return mockCareers.find((career) => career.id === id);
};

// Export careers by interest tags
export const getCareersByInterests = (interestTags) => {
  return mockCareers.filter((career) =>
    career.interestTags.some((tag) =>
      interestTags.some(
        (interest) =>
          tag.toLowerCase().includes(interest.toLowerCase()) ||
          interest.toLowerCase().includes(tag.toLowerCase())
      )
    )
  );
};
